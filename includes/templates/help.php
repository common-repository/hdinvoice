	<h3>
		Can I delete an invoice or customer?
	</h3>
	<div>
		<p>
			It is strongly encouraged to never delete an invoice, but to instead void the invoice. This helps keep the numbering system and statistics intact. You can void an invoice by first editing it, then selecting <strong>VOID INVOICE</strong> located in the header. You can also unvoid an invoice at anytime.
		</p>
		<p>
			However, if you really need to delete an invoice altogether, then you can do so by accessing <strong>Tools</strong>, and then selecting the <strong>Admin CPT</strong> tool (Admin Custom Post Type). This will show you the actual wordpress admin area that houses invoice, similar to posts or pages. <strong>Admin Taxonomy</strong> is how you would view the customer admin area. <strong>These tools should be used with caution!</strong>
		</p>
	</div>
</div>

